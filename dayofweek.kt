fun main(args : Array<String>) {
        val number = "2"
        when(number) {
            "1" -> print("Sunday")
            "2" -> print("Monday")
            "3" -> print("Tuesday")
            "4" -> print("Wednesday")
            "5" -> print("Thursday")
            "6" -> print("Friday")
            "7" -> print("Saturday")
            else -> print("this is not a valid number please try again")
    }
}